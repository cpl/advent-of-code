package y2021

import (
	"fmt"
	"testing"
)

func TestTrickShot(t *testing.T) {
	t.Parallel()

	ts := ParseTrickShot("target area: x=20..30, y=-10..-5")
	fmt.Printf("%+v\n", ts)

	ts.Shoot(7, 0, 20)
	ts.Print(-5, 10, 40, -20)
}

